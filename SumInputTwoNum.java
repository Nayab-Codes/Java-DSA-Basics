import java.util.*;
public class SumInputTwoNum {
    public static void main(String [] args){
        Scanner sc = new Scanner(System.in);
       System.out.println("Enter First Number A:");
       int A = sc.nextInt();
       System.out.println("Enter Second Number B:");
       int B = sc.nextInt();
       int sum = A + B;
       System.out.println(sum);
    }
}
